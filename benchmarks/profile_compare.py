"""
Benchmark helper to compare baseline vs PyTraceFlow runs using cProfile.

Usage examples:
  python benchmarks/profile_compare.py --target benchmarks/trace_stress.py
  python benchmarks/profile_compare.py --target your_script.py --target-args "--foo 1 --bar 2"

Outputs:
  - baseline.prof (cProfile of plain run)
  - traced.prof   (cProfile of run wrapped with pytraceflow.py)
  - pft.json      (trace generated by PyTraceFlow)

You can inspect the profiles with: python -m pstats baseline.prof
"""

from __future__ import annotations

import argparse
import shlex
import subprocess
import sys
import time
from pathlib import Path


def run_cmd(cmd: list[str], label: str) -> float:
    print(f"[benchmark] running {label}: {' '.join(shlex.quote(c) for c in cmd)}")
    start = time.perf_counter()
    completed = subprocess.run(cmd, stdout=subprocess.PIPE, stderr=subprocess.PIPE, text=True)
    duration = time.perf_counter() - start
    print(f"[benchmark] {label} finished in {duration:.3f}s, returncode={completed.returncode}")
    if completed.stdout:
        print(f"stdout:\n{completed.stdout}")
    if completed.stderr:
        print(f"stderr:\n{completed.stderr}")
    return duration


def main():
    parser = argparse.ArgumentParser(description="Compare baseline vs PyTraceFlow runs")
    parser.add_argument("--target", required=True, help="Script to execute")
    parser.add_argument(
        "--target-args",
        default="",
        help="Arguments string passed to the target script (quotes required for multiple args)",
    )
    parser.add_argument("--output-dir", default="bench-output", help="Where to store profile/json outputs")
    parser.add_argument(
        "--flush-interval",
        type=float,
        default=5.0,
        help="PyTraceFlow flush interval in seconds (use <=0 to disable periodic flush)",
    )
    parser.add_argument(
        "--flush-call-threshold",
        type=int,
        default=500,
        help="Flush after N new calls; 0 disables this threshold",
    )
    parser.add_argument(
        "--skip-inputs",
        action="store_true",
        help="Pass --skip-inputs to PyTraceFlow to reduce serialization overhead",
    )
    args = parser.parse_args()

    out_dir = Path(args.output_dir)
    out_dir.mkdir(parents=True, exist_ok=True)

    target_args_list = shlex.split(args.target_args)

    baseline_prof = out_dir / "baseline.prof"
    traced_prof = out_dir / "traced.prof"
    trace_json = out_dir / "pft.json"

    baseline_cmd = [
        sys.executable,
        "-m",
        "cProfile",
        "-o",
        str(baseline_prof),
        args.target,
        *target_args_list,
    ]
    traced_cmd = [
        sys.executable,
        "-m",
        "cProfile",
        "-o",
        str(traced_prof),
        "pytraceflow.py",
        "-s",
        args.target,
        "-o",
        str(trace_json),
        "--flush-interval",
        str(args.flush_interval),
        "--flush-call-threshold",
        str(args.flush_call_threshold),
    ]
    if args.skip_inputs:
        traced_cmd.append("--skip-inputs")
    traced_cmd.extend(target_args_list)

    t1 = run_cmd(baseline_cmd, "baseline")
    t2 = run_cmd(traced_cmd, "pytraceflow")

    print("\nSummary")
    print(f"baseline:    {t1:.3f}s -> {baseline_prof}")
    print(f"pytraceflow: {t2:.3f}s -> {traced_prof}")
    print(f"trace json:  {trace_json}")


if __name__ == "__main__":
    main()
